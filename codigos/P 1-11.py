# ==, !=, >=, <=, <, >
print("\n")
print("-----Operador IGUAL (==)")
print(5 == 7) #False 
print(5 == 5) #True
print(5.0 == 5) #True 
print("\n") 

print("-----Operador DIFERENTE o NO IGUAL (!=)")
print(5 != 5) #False
print(10 != 7) #True
print(5.0 != 5) #False
print("\n")

print("-----Operador MAYOR QUE (>)")
print(10 > 5) #True
print(3 > 9) #False
print(8 > 8) #False
print("\n")

print("-----Operador MENOR QUE (<)")
print(5 < 8) #True
print(12 < 6) #False
print(0 > 0) #False
print("\n")

print("-----Operador MENOR o IGUAL QUE (<=)")
print(4 <= 7) #True
print(11 <= 2) #False
print(1 <= 1) #True
print("\n")

print("-----Operador MAYOR o IGUAL QUE (>=)")
print(13 >= 3) #True
print(6 >= 6) #True
print(14 >= 20) #False
print("\n")
