#.................................................................................................. 
radio = float(input("Ingresa el valor del radio: ")) 
área = 3.1416 * radio ** 2 
perímetro = 3.1416 * 2 * radio 
print("El área del círculo es: " + str(área)) 
print("El perímetro del círculo es: " + str(perímetro)) 
