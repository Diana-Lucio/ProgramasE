print("\n")
print("Imprime los valores del 0 al 9")
x = range(10)
for num in x:
    print(num)
print("\n")
print("Imprime los valores del 5 al 15")
x1 = range(5, 16)
for num in x1:
    print(num)
print("\n")
print("Imprime los pares del 10 al 20")
x2 = range(10, 21, 2)
for num in x2:
    print(num)
print("\n")
print("Imprime los impares del 11 al 21")
x3 = range(11, 22, 2)
for num in x3:
    print(num)
print("\n")
